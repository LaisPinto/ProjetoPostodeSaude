    private void preencherProntuario(){
        try {
            ProntuarioControle pc = new ProntuarioControle();
            
            ArrayList<String> te = new ArrayList();
            
            te = pc.preencherProntuario();
            for(int i = 0; i <te.size(); i++){
                jcConsulta.addItem(te.get(i));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                this, "Erro! " + e.getMessage());
        }
    }
    
    private void preencherTabela() {
        try {

            ProntuarioControle p = new ProntuarioControle();

            ArrayList<Prontuario> pron = new ArrayList<>();

            pron = p.preencherTabela();
            dtm.setNumRows(0);
            for (int i = 0; i <pron.size(); i++) {
                dtm.addRow(new Object[]{
                    String.valueOf(pron.get(i).getIdprontuario()),
                    String.valueOf(pron.get(i).getpaciente()),
                    String.valueOf(pron.get(i).juntarDataTabela()),
                    String.valueOf(pron.get(i).getconsulta())
                     
            });
            }//fecha for

            /* Adicionando o modelo de tabela 
             com os dados na tabela produto */
            jtDados.setModel(dtm);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Erro! " + e.getMessage());
        }//fecha catch
    }//fecha preencherTabela
    
    private void alterar() {
        Prontuario p = new Prontuario();
        int linha = jtDados.getSelectedRow();
            if(linha != -1) {
            jtfCodigo.setText((String)jtDados.getValueAt(linha, 0));
            jtfPaciente.setText((String)jtDados.getValueAt(linha, 1));
            p.Data((String)jtDados.getValueAt(linha, 2));
            jtfDia.setText(p.getDia());
            jtfMes.setText(p.getMes());
            jtfAno.setText(p.getAno());
            jtfHora.setText(p.gethora());
            jtfMin.setText(p.getmin());
            
            
            }else{
                JOptionPane.showMessageDialog(this, 
                        "Você não selecionou nenhuma linha!");
         }//fecha else
    }//fecha método
    
