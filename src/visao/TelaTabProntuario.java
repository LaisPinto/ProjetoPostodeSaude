      private void confirmarAlteracao(){
        try {
            Prontuario p = new Prontuario();
            ProntuarioControle pc = new ProntuarioControle();
            p.setIdProntuario(Integer.parseInt(jtfCodigo.getText()));
            p.setConsultatipo(pc.procurarId(jcConsulta.getSelectedItem().toString()));
            p.setpaciente(jtfPaciente.getText());
            p.setAno(jtfAno.getText());
            p.setMes(jtfMes.getText());
            p.setDia(jtfDia.getText());
            p.sethora(jtfHora.getText());
            p.setmin(jtfMin.getText());
            
            pc.alterarProntuario(p.getIdprontuario(),p.getAno(), p.getMes(), p.getDia(), p.gethora(), p.getmin(), p.getConsultatipo());
            
            JOptionPane.showMessageDialog(
                    this,
                    "Produto alterado com sucesso!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                    "erro! " + e.getMessage());
        }
    }
    
