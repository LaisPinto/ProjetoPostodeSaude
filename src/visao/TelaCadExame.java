   private void cadastrar(){
        try {
            Exame e = new Exame();
            ExameControle ec = new ExameControle();
            e.setIdpaciente(ec.procurarIdPaciente(jcPaciente.getSelectedItem().toString()));
            e.setExametipo(ec.procurarId(jcExame.getSelectedItem().toString()));
            e.setAno(jtfAno.getText());
            e.setMes(jtfMes.getText());
            e.setDia(jtfDia.getText());
            e.sethora(jtfHora.getText());
            e.setmin(jtfMin.getText());
            
            
            
            
            ec.cadastrarExame(e.getIdpaciente(), e.getAno(), e.getMes(), e.getDia(), e.gethora(), e.getmin(), e.getExametipo());
            
            JOptionPane.showMessageDialog(
                    rootPane,
                    "Produto cadastrado com sucesso!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                    "erro! " + e.getMessage());
        }
    }//fecha método cadastrar
    
