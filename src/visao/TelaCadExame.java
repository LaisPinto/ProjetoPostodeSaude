    private void preencherExame(){
        try {
            ExameControle e = new ExameControle();
            ArrayList<String> te = new ArrayList();
            te = e.preencherExame();
            
            for (int i = 0; i <te.size(); i++){
                jcExame.addItem(te.get(i));
            }
       } catch (Exception e) {
            JOptionPane.showMessageDialog(
                this, "Erro! " + e.getMessage());
        }
    }
    
