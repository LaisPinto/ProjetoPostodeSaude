     private void confirmarAlteracao(){
        try {
            Exame e = new Exame();
            ExameControle ec = new ExameControle();
            
            e.setIdExame(Integer.parseInt(jtfCodigo.getText()));
            e.setExametipo(ec.procurarId(jcExame.getSelectedItem().toString()));
            e.setAno(jtfAno.getText());
            e.setMes(jtfMes.getText());
            e.setDia(jtfDia.getText());
            e.sethora(jtfHora.getText());
            e.setmin(jtfMin.getText());
            
            ec.alterarExame(e.getIdExame(), e.getAno(), e.getMes(), e.getDia(), e.gethora(), e.getmin(),e.getExametipo());
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                    "erro! " + e.getMessage());
        }
    }
     
