    private void preecherExame(){
        try {
            ExameControle ec = new ExameControle();
            
            ArrayList<String> te = new ArrayList();
            
            te = ec.preencherExame();
            
            for (int i = 0; i <te.size(); i++){
                jcExame.addItem(te.get(i));
            }//fecha for
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                this, "Erro! " + e.getMessage());
        }
    }
    
  
    
     private void preencherTabela() {
        try {

            ExameControle p = new ExameControle();

            ArrayList<Exame> e = new ArrayList<>();

            e = p.preencherTabela();
            dtm.setNumRows(0);
            for (int i = 0; i <e.size(); i++) {
                dtm.addRow(new Object[]{
                    String.valueOf(e.get(i).getIdExame()),
                    String.valueOf(e.get(i).getpaciente()),
                    String.valueOf(e.get(i).juntarDataTabela()),
                    String.valueOf(e.get(i).getexame())
                     
            });
            }//fecha for

            /* Adicionando o modelo de tabela 
             com os dados na tabela produto */
            jtDados.setModel(dtm);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Erro! " + e.getMessage());
        }//fecha catch
    }//fecha preencherTabela
    
    private void alterar() {
        Exame e = new Exame();
        int linha = jtDados.getSelectedRow();
            if(linha != -1) {
            jtfCodigo.setText((String)jtDados.getValueAt(linha, 0));
            jtfPaciente.setText((String)jtDados.getValueAt(linha, 1));
            e.Data((String)jtDados.getValueAt(linha, 2));
            jtfDia.setText(e.getDia());
            jtfMes.setText(e.getMes());
            jtfAno.setText(e.getAno());
            jtfHora.setText(e.gethora());
            jtfMin.setText(e.getmin());
            
          
            }else{
                JOptionPane.showMessageDialog(this, 
                        "Você não selecionou nenhuma linha!");
         }//fecha else
    }//fecha método
    
     private void limparTabela() {
      /* Para limpar a tabela temos que setar o número de
           linhas para zero no default table model */        
        dtm.setNumRows(0);
    }//fecha limparTabela
    
