     private void confirmarAlteracao(){
        try {
            Agenda a = new Agenda();
            AgendaControle ac = new AgendaControle();
            
            a.setid_Agenda(Integer.parseInt(jtfCodigo.getText()));
            a.setExametipo(ac.procurarId(jComboBox1.getSelectedItem().toString()));
            a.setPaciente(jtfPaciente.getText());
            a.setAno(jtfAno.getText());
            a.setMes(jtfMes.getText());
            a.setDia(jtfDia.getText());
            a.sethora(jtfHora.getText());
            a.setmin(jtfMin.getText());
            
            ac.alterarAgenda(a.getid_Agenda(), a.getAno(), a.getMes(), a.getDia(), a.gethora(), a.getmin(),a.getExametipo());
                    
            JOptionPane.showMessageDialog(
                    this,
                    "Produto alterado com sucesso!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                    "erro! " + e.getMessage());
        }
    }
    
