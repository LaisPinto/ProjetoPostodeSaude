    private void preencherAgenda(){
        try {
            AgendaControle ac = new AgendaControle();
            
            ArrayList<String> ta = new ArrayList();
            
            ta = ac.preencherAgenda();
            for (int i = 0; i <ta.size(); i++){
                jComboBox1.addItem(ta.get(i));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                this, "Erro! " + e.getMessage());
        }
    }
    
    private void preencherTabela() {
        try {
            
            AgendaControle a = new AgendaControle();

            ArrayList<Agenda> age = new ArrayList<>();
            
            age = a.preencherTabela();
            dtm.setNumRows(0);
            for (int i = 0; i <age.size(); i++) {
                dtm.addRow(new Object[]{
                    String.valueOf(age.get(i).getid_Agenda()),
                    String.valueOf(age.get(i).getPaciente()),
                    String.valueOf(age.get(i).juntarDataTabela()),
                    String.valueOf(age.get(i).getexame())
                     
            });
            }//fecha for

            /* Adicionando o modelo de tabela 
             com os dados na tabela Agenda */
            jtDados.setModel(dtm);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Erro! " + e.getMessage());
        }//fecha catch
    }//fecha preencherTabela
    
    private void alterar() {
        Agenda a = new Agenda();
        int linha = jtDados.getSelectedRow();
            if(linha != -1) {
            jtfCodigo.setText((String)jtDados.getValueAt(linha, 0));
            jtfPaciente.setText((String)jtDados.getValueAt(linha, 1));
            a.Data((String)jtDados.getValueAt(linha, 2));
            jtfDia.setText(a.getDia());
            jtfMes.setText(a.getMes());
            jtfAno.setText(a.getAno());
            jtfHora.setText(a.gethora());
            jtfMin.setText(a.getmin());
            
            
          
            }else{
                JOptionPane.showMessageDialog(this, 
                        "Você não selecionou nenhuma linha!");
         }//fecha else
    }//fecha método
    
